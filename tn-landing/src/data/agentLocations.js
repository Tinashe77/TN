// Get unique towns for filtering
export const getUniqueTowns = () => {
  const towns = [...new Set(agentLocations.map(agent => agent.town))]
  return towns.sort()
}

// Search agents by name, location, or town
export const searchAgents = (query, townFilter = '') => {
  const searchTerm = query.toLowerCase().trim()

  return agentLocations.filter(agent => {
    const matchesSearch = !searchTerm ||
      agent.name.toLowerCase().includes(searchTerm) ||
      agent.location.toLowerCase().includes(searchTerm) ||
      agent.town.toLowerCase().includes(searchTerm)

    const matchesTown = !townFilter || agent.town === townFilter

    return matchesSearch && matchesTown
  })
}
